#include "3-calc.h"
#include <stdio.h>
/**
 * op_add - function to sum two integers
 * @a: first integers
 * @b: second integer
 * Return: Sum of integers
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function to subtract two integers
 * @a: first integers
 * @b: second integer
 * Return: Sub of integers
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function to multiply two integers
 * @a: first integers
 * @b: second integer
 * Return: Mult. of integers
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function to divide two integers
 * @a: first integers
 * @b: second integer
 * Return: Division of integers
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function to find remainder two integers
 * @a: first integers
 * @b: second integer
 * Return: Modulus of integers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
